							'password' => 'traffic',
							'status' => 1,
							'type' => 1
						)
					)
				);
			}
			if ($event['create'] == 'file_types') {
				$file_type = ClassRegistry::init('FileType');
				$arr_file_types = array(
					'Montages',
					'Plans & Drawings',
					'Scope & Quotes',
					'Completed Photos',
					'OH&S Insurance & Permits'
				);
				foreach ($arr_file_types as $f_type) :
					$file_type->create();
					$file_type->save(
						array('FileType' =>
							array(
								'name' => $f_type
							)
						)
					);
				endforeach;
			}
		}
	}

